package com.uesc.lif.i2ot.ontology.model;

public class SmartObjectOntProperties {
	private String canBeIn;
	private String canBeMovedBy;
	private String isMovedBy;
	private String ofMaterial;
	private String hasLocation;
	private String deviceTag;
	
	
	public String getCanBeIn() {
		return canBeIn;
	}
	public void setCanBeIn(String canBeIn) {
		this.canBeIn = canBeIn;
	}
	public String getCanBeMovedBy() {
		return canBeMovedBy;
	}
	public void setCanBeMovedBy(String canBeMovedBy) {
		this.canBeMovedBy = canBeMovedBy;
	}
	public String getIsMovedBy() {
		return isMovedBy;
	}
	public void setIsMovedBy(String isMovedBy) {
		this.isMovedBy = isMovedBy;
	}
	public String getOfMaterial() {
		return ofMaterial;
	}
	public void setOfMaterial(String ofMaterial) {
		this.ofMaterial = ofMaterial;
	}
	public String getHasLocation() {
		return hasLocation;
	}
	public void setHasLocation(String hasLocation) {
		this.hasLocation = hasLocation;
	}
	public String getDeviceTag() {
		return deviceTag;
	}
	public void setDeviceTag(String deviceTag) {
		this.deviceTag = deviceTag;
	}

	
}
